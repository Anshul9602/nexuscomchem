      <h2 class="desc-title mb-3">Why Splits are hard to Process? </h2>
      <p>
        The seeds of sickle pods are naturally hard to break. It can be done by soaking or roasting them so that; they become flexible for further processing. The splits on the other hand are also soaked to provide them high moisture content with the help of flaker machine. Splits obtained through flaker machine are then sent for grinding and drying. Since, the soaked condition also becomes difficult for processing, direct grinding is not recommended as it will reduce hydration quality of the product. Therefore, step by step method is followed in this regard.
      </p>
      <h2 class="desc-title mb-3">About</h2>
      <p>
        The guar gum splits is basically the endosperm part of the guar seed which is commercially extracted from the seeds by a mechanical process of cracking, roasting, differential attrition, sieving and polishing. The seeds are crack opened and the germ is separated from the endosperm. Two halves of the endosperm are obtained from each seed and the fine layer of fibrous material, which forms the husk, is removed and separated from the endosperm halves by the process of dehusking and polishing.<br><br>
        Guar gum refined splits are derived from Haryana Seeds and have high viscosity potential together with higher purity and gum; The splits are manufactured in the state of the Art technology new plant equipped with abundant capacity silo storage infrastructure for raw material, modern warehouse for finished product, Q. C. Lab manned by experienced technicians.<br><br>
        Guar gum refined splits are available in Double Refined (DPS)and Triple Refined (TPS) specifications and packing size of 50 Kilo PP Bag / 1000 kilo super sack/ 22000 kilo Container liner bag. <br><br>
      </p>

