      <h2 style="margin-top:20px;">About </h2>
      <p>
        Gum is derived from guar seeds or cyamopsis tetragonoloba termed
        as Guar Gum. Guar Gum can also be termed as guaran. These seeds
        have high low-shear viscosity as evaluated with other
        hydrocolloids like (locust bean gum). Guar Gums are effective
        thickeners and stabilizers. The Organic Guar gum is derived when
        the seeds of this plant are being processed and separated from the
        endosperm. These plants are also known as a once-a-year
        plant because they can be grown even in the monsoon season or in
        the summer season and are the plant from leguminous family.<br><br>
        Guar Gum is relatively cost effective as compared to other
        thickeners and stabilizers along with it being an effective
        binder, plasticizer and emulsifier. One of the important
        properties of guar gum, a polysaccharide, is that it is high on
        galactose and mannose. Guar gum is also known as guarkernmehl,
        guaran, goma guar, gomme guar and galactomannan.<br><br>

